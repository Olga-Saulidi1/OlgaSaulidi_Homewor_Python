class Smartphone:
    def __init__(self, brand, model, phone_number):
        self.brand = brand
        self.model = model
        self.phone_number = phone_number

    def get_brand (self):
        return self.brand
    
    def get_model (self):
        return self.model
    
    def get_phone_number (self):
        return self.phone_number
    